    private void btnregistarUsuarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnregistarUsuarioActionPerformed
//DATOS DEL REGISTRO
        String nombre = txtnom.getText();
        //String apellido = txtap.getText();
        String usuario = txtus.getText();
        String password = txtpas.getText();
        
  //GUARDAR EN LA BASE DE DATOS
        try{
           PreparedStatement ps = (PreparedStatement) cn.prepareStatement("INSERT INTO usuario(nombre,usuario,pass)VALUES(?,?,?)");
           ps.setString(1, nombre);
        //   ps.setString(2,apellido);
           ps.setString(2, usuario);
           ps.setString(3, password);
           ps.executeUpdate();
           JOptionPane.showMessageDialog(null, "USUARIO REGISTRADO EXITOSAMENTE");
           limpiar();
           loginFrame ns = new loginFrame();
            ns.setVisible(true);
            this.dispose();
           //mostrardatos();
          
       }catch (SQLException e){
           System.out.println("ERROR AL REGISTRAR NUEVO USUARIO"+e);
       }
        
          
    }//GEN-LAST:event_btnregistarUsuarioActionPerformed

